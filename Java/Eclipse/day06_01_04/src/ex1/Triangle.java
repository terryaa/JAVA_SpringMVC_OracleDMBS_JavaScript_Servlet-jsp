package ex1;

public class Triangle extends Draw{
	private int width;
	private int height;
	public Triangle() {
		width=10;
		height=20;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}

}
